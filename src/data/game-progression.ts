const UNLISTED_LOCATION_STAGE = 9_999;

export function progressionStageForSpecies(
  species: string,
  game: Game,
  locationsFile: LocationsFile | null,
): number {
  return progressionDetailForSpecies(species, game, locationsFile).stage;
}

export interface ProgressionDetail {
  stage: number;
  earliestLocation?: string;
  availableInGame: boolean;
}

export function progressionDetailForSpecies(
  species: string,
  game: Game,
  locationsFile: LocationsFile | null,
): ProgressionDetail {
  if (!locationsFile) return { stage: Infinity, availableInGame: false };
  const entry = locationsFile.species[species];
  if (!entry) return { stage: Infinity, availableInGame: false };
  const gameInfo = GAME_BY_ID[game];
  const map = PROGRESSION[game];
  let min = Infinity;
  let minLocation: string | undefined;
  let sawAnyLocation = false;
  for (const group of entry.groups) {
    if (!group.games.some((g) => gameInfo.pokemondbGames.includes(g))) continue;
    for (const loc of group.locations) {
      sawAnyLocation = true;
      const stage = map[loc.name] ?? UNLISTED_LOCATION_STAGE;
      if (stage < min) {
        min = stage;
        minLocation = loc.name;
      }
    }
  }
  if (!sawAnyLocation) return { stage: Infinity, availableInGame: false };
  return { stage: min, earliestLocation: minLocation, availableInGame: true };
}
